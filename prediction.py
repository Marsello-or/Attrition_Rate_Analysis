import pandas as pd
import numpy as np
import joblib
import sys
import os

def run_prediction(input_csv_path, output_csv_path):
    print("="*65)
    print("   JAYA JAYA MAJU - AI EMPLOYEE ATTRITION PREDICTOR (CATBOOST)   ")
    print("="*65)

    # 1. VERIFIKASI FILE ARTEFAK MODEL
    model_file = 'model_attrition_catboost.pkl'
    scaler_file = 'scaler_attrition.pkl'
    feature_file = 'feature_columns.pkl'

    if not (os.path.exists(model_file) and os.path.exists(scaler_file) and os.path.exists(feature_file)):
        print("[ERROR] Artefak model tidak lengkap di folder aktif!")
        print("Pastikan file berikut ada di folder yang sama dengan script ini:")
        print(f" - {model_file}\n - {scaler_file}\n - {feature_file}")
        return

    # 2. LOAD ARTEFAK MODEL & PREPROCESSING TOOLS
    try:
        model = joblib.load(model_file)
        scaler = joblib.load(scaler_file)
        expected_columns = joblib.load(feature_file)
        print("[INFO] Artefak model CatBoost & Scaler berhasil dimuat.")
    except Exception as e:
        print(f"[ERROR] Gagal memuat objek pkl. Sifat error: {e}")
        return

    # 3. LOAD DATA INPUT KARYAWAN BARU
    try:
        df_new = pd.read_csv(input_csv_path)
        df_scoring = df_new.copy()
        print(f"[INFO] Data input '{input_csv_path}' berhasil dimuat ({len(df_new)} baris).")
    except Exception as e:
        print(f"[ERROR] Gagal membaca file input CSV. Sifat error: {e}")
        return

    # 4. PREPROCESSING OTOMATIS & ALIGNMENT FITUR
    # Hapus kolom ID dan Target jika tidak sengaja disertakan dalam data pengujian baru
    columns_to_drop = ['EmployeeId', 'Attrition']
    for col in columns_to_drop:
        if col in df_scoring.columns:
            df_scoring = df_scoring.drop(columns=[col])

    # Transformasi variabel kategorikal menggunakan One-Hot Encoding
    df_encoded = pd.get_dummies(df_scoring, drop_first=True)

    # Penyelarasan kolom agar struktur dummy persis sama dengan saat training model
    for col in expected_columns:
        if col not in df_encoded.columns:
            df_encoded[col] = 0 # Berikan nilai 0 jika kolom kategorikal tersebut tidak muncul di data baru

    # Pastikan urutan kolom sesuai urutan fitur model, dan buang kolom baru yang tidak dikenali model
    df_encoded = df_encoded[expected_columns]

    # Eksekusi penskalaan fitur menggunakan StandardScaler yang dimuat
    X_scaled = scaler.transform(df_encoded)

    # 5. EKSEKUSI PREDIKSI DENGAN CUSTOM THRESHOLD (0.35)
    print("[INFO] Menghitung estimasi skor risiko kecerdasan buatan...")

    # Ambil nilai probabilitas untuk kelas Resign (1)
    probabilities = model.predict_proba(X_scaled)[:, 1]

    # Terapkan ambang batas keputusan optimal bisnis yang sensitif (0.35)
    custom_threshold = 0.35
    predictions = (probabilities >= custom_threshold).astype(int)

    # 6. PENEMPELAN INSIGHT PREDIKSI KE DATAFRAME ASLI KARYAWAN
    df_new['Risk_Score (%)'] = (probabilities * 100).round(2)
    df_new['Risk_Category'] = df_new['Risk_Score (%)'].apply(
        lambda x: 'High Risk' if x >= 70 else ('Medium Risk' if x >= 40 else 'Low Risk')
    )
    df_new['Prediction_Attrition'] = predictions

    # 7. EKSPOR HASIL PREDIKSI KE CSV BARU
    try:
        df_new.to_csv(output_csv_path, index=False)
        print(f"[SUCCESS] Prediksi selesai secara robust!")
        print(f"[SUCCESS] Hasil analitik risiko disimpan ke: '{output_csv_path}'")

        # Tampilkan ringkasan singkat statistik deteksi
        high_risk_count = sum(df_new['Risk_Category'] == 'High Risk')
        print(f"[SUMMARY] Terdeteksi {high_risk_count} karyawan berkategori 'High Risk' yang butuh penanganan HR.")
    except Exception as e:
        print(f"[ERROR] Gagal mengekspor file hasil. Sifat error: {e}")

    print("="*65)

if __name__ == "__main__":
    # Mendukung eksekusi argumen terminal secara dinamis:
    # Contoh penggunaan: python predict.py data_input.csv data_output.csv
    if len(sys.argv) > 2:
        run_prediction(sys.argv[1], sys.argv[2])
    else:
        # Nama file bawaan (default) jika dijalankan langsung tanpa parameter terminal
        run_prediction('df_clean.csv', 'JayaJayaMaju_Predictions_Output.csv')